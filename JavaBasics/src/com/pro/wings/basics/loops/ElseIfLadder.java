package com.pro.wings.basics.loops;

public class ElseIfLadder {

	public static void main(String[] args) {
		
		int i =5;
		
		if(i>5)
		{
			System.out.println("greater than 5");
		}
		else if(i<5)
		{
			System.out.println("less than 5");
		}
		
		else
		{
			System.out.println("eqauls to 5");
		}
		
		
	}

}
